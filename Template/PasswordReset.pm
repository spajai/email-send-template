use Data::Dumper;

#----------------------------------------------------------------------------------#
#----------------------------------------------------------------------------------#
has 'common' => (
    is            => 'ro',
    isa           => 'MyApp::Email::Template::Common',
    default       => sub { MyApp::Email::Template::Common->new },
    lazy          => 1,
);

#----------------------------------------------------------------------------------#
#----------------------------------------------------------------------------------#

sub build_template {
    my ( $self, $data, $conf ) = @_;

    my $template = $self->common->header($data, $conf );
    
    
    $template .= $self->_password_reset_template($data, $conf);

    $template .= $self->common->footer($data, $conf);

    return $template;
}
#----------------------------------------------------------------------------------#
# template goes here
#----------------------------------------------------------------------------------#
sub _password_reset_template {
    my ( $self, $data, $conf ) = @_;


    my $template_body = <<TEMPLATE;


    <b>OTP</b>   :- $data->{otp}
    <b>TOKEN</b> :- $data->{token}





TEMPLATE


    return $template_body;

}
