# custom module                                                                    #
#----------------------------------------------------------------------------------#
use AuthServer::Email::Template::Common;
#----------------------------------------------------------------------------------#
#----------------------------------------------------------------------------------#
#remove later
use Data::Dumper;

#----------------------------------------------------------------------------------#
#----------------------------------------------------------------------------------#
has 'common' => (
    is            => 'ro',
    isa           => 'AuthServer::Email::Template::Common',
    default       => sub { AuthServer::Email::Template::Common->new },
    lazy          => 1,
    documentation => 'This variable will have object of AuthServer::Email::Template::Common',
);

#----------------------------------------------------------------------------------#
#----------------------------------------------------------------------------------#

sub build_template {
    my ( $self, $data, $conf ) = @_;

    my $template = $self->common->header($data, $conf );
    
    
    $template .= $self->_welcome_template($data, $conf);

    $template .= $self->common->footer($data, $conf);

    return $template;
}
#----------------------------------------------------------------------------------#
# template goes here
#----------------------------------------------------------------------------------#
sub _welcome_template {
    my ( $self, $data, $conf ) = @_;


    my $template_body = <<TEMPLATE;


    <b>OTP</b>   :- $data->{otp}
    <b>TOKEN</b> :- $data->{token}





TEMPLATE


    return $template_body;

}
#----------------------------------------------------------------------------------#
#----------------------------------------------------------------------------------#

__PACKAGE__->meta->make_immutable;

